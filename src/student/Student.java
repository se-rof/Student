package student;

public class Student {
    private String name;
    private String address;
    
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    public String getAddress(String address) {
        return address;
    }
    
    public void setAddress(String address) {
        this.address = address;
    }

}
